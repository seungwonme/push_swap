#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = ft_calloc(1, sizeof(t_list));
	if (!new)
		return (NULLPTR);
	new->content = content;
	new->next = NULLPTR;
	return (new);
}
